package M1;
public class FirstJava {
  public static void main(String[] args) {
    byte number0 = 127; // -128 to 127 -- 1 byte or 8 bits
    short number1 = 32000; // -32,768 to 32,767 -- 2 bytes
    char ch = 'a'; // 2 bytes
    int number2 = 25; // -2,147,483,647 to 2,147,483,647 -- 4 bytes
    long number3 = 1957394923; // 8 bytes
    float number4 = 10.5f; // 4 bytes
    double number5 = 100.345; // 8 bytes
    String name = "NK";
    System.out.println("Welcome to my World");
    System.out.println(ch);
    System.out.println(number0);
    System.out.println(number1);
    System.out.println(number2);
    System.out.println(number3);
    System.out.println(number4);
    System.out.println(number5);
    System.out.println(name);
  }
}